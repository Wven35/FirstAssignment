public class ForCounter {
    public static void main(String[] args) {

        // loop through 10 iterations and print the counter.
        for (int x = 0; x < 10; x++) {
            System.out.printf("%d ", x);
        } // for

        // print a single blank line.
        System.out.println();

        // Prints out 1 to 10.
        for (int x = 1; x < 10; x+= 2) {
            System.out.printf("%d ",x);
        } // for
    } // main
 } // ForCounter