public class DoghouseBlueprint {

    // Instance Variables!
    private int height;
    private int width;
    private String name;
    private String color;

    public DoghouseBlueprint(String name) {
    }

    public void DoghouseBlueprint(String name){
        this.name = name;
        this.color = "Blue";
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }





} // end public class
